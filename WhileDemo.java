public class WhileDemo {
    public static void main(String[] args) {

        //Zählervariable anlegen
        int count = 0;

        //Bedingung für while definieren
        while (count <= 10) {
            //Schleifenstart
            System.out.println("Durchlauf: " + count);

            count ++;
            //Schleifenende


        }
    }
}
